using System;

namespace LoopFor
{
    class Program
    {
        static void Main(string[] args)
        {

            //Definición del bucle (variable con valor inicial; condición; iterador o contador)

            for (int i = 0; i < 15; i++)
            {
                Console.WriteLine("Ejemplo de bucle for: " + i);
            }

            Console.ReadKey();
        }
    }
}
